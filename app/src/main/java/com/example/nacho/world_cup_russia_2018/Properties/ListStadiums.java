public class ListStadiums {

    private int idStadium;
    private String nameStadium;
    private String cityStadium;
    private long latStadium;
    private long longStadium;

    public int getIdStadium() {
        return idStadium;
    }

    public void setIdStadium(int idStadium) {
        this.idStadium = idStadium;
    }

    public String getNameStadium() {
        return nameStadium;
    }

    public void setNameStadium(String nameStadium) {
        this.nameStadium = nameStadium;
    }

    public String getCityStadium() {
        return cityStadium;
    }

    public void setCityStadium(String cityStadium) {
        this.cityStadium = cityStadium;
    }

    public long getLatStadium() {
        return latStadium;
    }

    public void setLatStadium(long latStadium) {
        this.latStadium = latStadium;
    }

    public long getLongStadium() {
        return longStadium;
    }

    public void setLongStadium(long longStadium) {
        this.longStadium = longStadium;
    }
}
